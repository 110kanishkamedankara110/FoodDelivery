    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getRowCount() == 0) {
            new Dialog(this, true, "Add Food", Types.WARNING).setVisible(true);

        } else {
            
            HashMap<Food,Integer>foodList=new HashMap();
            
            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            for (int i = 0; i < table.getRowCount(); i++) {
                String food = (String) table.getValueAt(i, 0);
                int qty = Integer.parseInt((String) table.getValueAt(i, 1));
                double price =Double.parseDouble((String) table.getValueAt(i, 2));
                String commands=(String) table.getValueAt(i, 3);
                
                Food f=new Food.Builder().setName(food).setPrice(price).setCommands(commands).getInstance();
                foodList.put(f, qty);
                
            }
            
            Order order=OrderCreater.createOrder(foodList);
            OrderManager om=new OrderManager();
            om.placeOrder(order);
            
            clear();
           table.setRowCount(0);
            
        }

    }//GEN-LAST:event_jButton3ActionPerformed

