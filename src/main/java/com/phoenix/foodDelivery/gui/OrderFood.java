    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedRow() != -1) {
            if (evt.getClickCount() == 2) {
                if (jTable1.getSelectedColumn() == 4) {
                    yesNoDialog yesno = new yesNoDialog(this, true, "You Wish To Delete This", Types.SUCCESS);
                    if (yesno.getReturnStatus() == 1) {
                        ((DefaultTableModel) jTable1.getModel()).removeRow(jTable1.getSelectedRow());
                    }

                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String food = jTextField2.getText();
        String qty = jTextField4.getText();
        String price = jTextField5.getText();
                String commands = jTextArea1.getText();


        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        if (food.isBlank()) {
            new Dialog(this, true, "Insert Food", Types.WARNING).setVisible(true);

        } else if (qty.isBlank()) {
            new Dialog(this, true, "Insert Quantity", Types.WARNING).setVisible(true);

        } else if (price.isBlank()) {
            new Dialog(this, true, "Insert Price", Types.WARNING).setVisible(true);

        } else {
            Vector v = new Vector();
            v.add(food);
            v.add(qty);
            v.add(price);
            v.add(commands);
            v.add("Delete");

            tableModel.addRow(v);
            clear();
        }

    }//GEN-LAST:event_jButton2ActionPerformed
